// 50+ achievements. Each `test` is evaluated against the finished run; any number
// can unlock. Predicates reference stats, cooked %, the boss/events that fired,
// the chosen archetype, the raw answers, and the yap text.
const has = (ctx: { events: { id: string }[] }, id: string) =>
  ctx.events.some((e) => e.id === id);
const picked = (
  ctx: { answers: { qid: string; choiceId: string }[] },
  qid: string,
  choiceId: string
) => ctx.answers.some((a) => a.qid === qid && a.choiceId === choiceId);

